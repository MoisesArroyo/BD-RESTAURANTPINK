    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        // TODO add your handling code here:

        if(txtEmp.getText().isEmpty() || txtFecha.getText().isEmpty() || txtCliente.getText().isEmpty() || txtMesa.getText().isEmpty() ||
            txtID.getText().isEmpty() ||  txtPago.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }
        else{
            Pedidos ped = new Pedidos();
            ped.setPEDIDO_ID(Integer.parseInt(txtID.getText()));
            ped.setNUM_MESA(Integer.parseInt(txtMesa.getText()));
            ped.setPEDIDO_FECHA(java.sql.Date.valueOf(txtFecha.getText()));
            ped.setPEDIDO_EMPLEADO(Integer.parseInt(txtEmp.getText()));
            ped.setPEDIDO_METODO_PAGO(Integer.parseInt(txtPago.getText()));
            ped.setPEDIDO_CLIENTE(Integer.parseInt(txtCliente.getText()));

            String mensaje;
            try {
                mensaje = ebo.agregarPed(ped);
                JOptionPane.showMessageDialog(null,mensaje);
                limpiar();
                listarPed();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_InsertActionPerformed

