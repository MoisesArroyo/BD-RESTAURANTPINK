    private void tbPedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPedidoMouseClicked
        // TODO add your handling code here:
        int seleccion = tbPedido.rowAtPoint(evt.getPoint());
        txtID.setText((String) tbPedido.getValueAt(seleccion, 0));
        txtMesa.setText((String) tbPedido.getValueAt(seleccion, 1));
        txtFecha.setText((String) tbPedido.getValueAt(seleccion, 2));
        txtEmp.setText((String) tbPedido.getValueAt(seleccion, 3));
        txtPago.setText((String) tbPedido.getValueAt(seleccion, 4));
        txtCliente.setText((String) tbPedido.getValueAt(seleccion, 5));
    }//GEN-LAST:event_tbPedidoMouseClicked

