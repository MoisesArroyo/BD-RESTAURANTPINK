    private void btnInsert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsert1ActionPerformed
        if(txtPRODUCTO_ID.getText().isEmpty() || txtNombre.getText().isEmpty() || txtDetalles.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{
            DetalleProducto pro = new DetalleProducto();
            pro.setDETA_PRODUCTO_ID(Integer.parseInt(txtPRODUCTO_ID.getText()));
            pro.setDETA_INGREDIENTE_ID(Integer.parseInt(txtNombre.getText()));
            pro.setPORCION(Integer.parseInt(txtDetalles.getText()));
            String mensaje;
            try {
                mensaje = ebo.agregarDeta(pro);
                JOptionPane.showMessageDialog(null,mensaje);
                limpiar();
                listarDeta();
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(FrmEmpleado.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnInsert1ActionPerformed

