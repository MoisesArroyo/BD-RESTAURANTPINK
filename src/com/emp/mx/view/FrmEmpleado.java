    private void tbEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEmpleadoMouseClicked
        // TODO add your handling code here:
        int seleccion = tbEmpleado.rowAtPoint(evt.getPoint());
        txtEMPLEADO_ID.setText((String) tbEmpleado.getValueAt(seleccion, 0));
        txtNombre.setText((String) tbEmpleado.getValueAt(seleccion, 1));
        txtApellidoP.setText((String) tbEmpleado.getValueAt(seleccion, 2));
        txtApellidoM.setText((String) tbEmpleado.getValueAt(seleccion, 3));
        txtLada.setText((String) tbEmpleado.getValueAt(seleccion, 4));
        txtTelefono.setText((String) tbEmpleado.getValueAt(seleccion, 5));
        txtRFC.setText((String) tbEmpleado.getValueAt(seleccion, 6));
        txtCorreo.setText((String) tbEmpleado.getValueAt(seleccion, 7));
        txtRol.setText((String) tbEmpleado.getValueAt(seleccion,8));
        txtSuc.setSelectedItem(tbEmpleado.getValueAt(seleccion, 9));
        txtDomi.setText((String) tbEmpleado.getValueAt(seleccion, 10));
        
    }//GEN-LAST:event_tbEmpleadoMouseClicked

