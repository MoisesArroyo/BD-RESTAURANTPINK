    private void btnelimiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnelimiActionPerformed
        // TODO add your handling code here:
        if(txtEMPLEADO_ID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene El ID a borrar");
        }else{
            String mensaje;
            try {
                mensaje = ebo.eliminarEmple(Integer.parseInt(txtEMPLEADO_ID.getText()));
                JOptionPane.showMessageDialog(null,mensaje);
                limpiar();
                listarEmpleado();
            } catch (SQLException ex) {
                Logger.getLogger(FrmEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }     
        }
    }//GEN-LAST:event_btnelimiActionPerformed

    private void btnModifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifActionPerformed
        // TODO add your handling code here:
        if(txtEMPLEADO_ID.getText().isEmpty() || txtNombre.getText().isEmpty() || txtApellidoP.getText().isEmpty() || txtApellidoM.getText().isEmpty()
                || txtLada.getText().isEmpty() || txtTelefono.getText().isEmpty()|| txtRFC.getText().isEmpty() || txtRol.getText().isEmpty()
                || txtDomi.getText().isEmpty() || txtCorreo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{
            empleado emp = new empleado();
            emp.setEMPLEADO_ID(Integer.parseInt(txtEMPLEADO_ID.getText()));
            emp.setNAME_EMPLEADO(txtNombre.getText());
            emp.setAPELLIDOP_EMPLEADO(txtApellidoP.getText());
            emp.setAPELLIDOM_EMPLEADO(txtApellidoM.getText());
            emp.setLADA_EMPLEADO(Integer.parseInt(txtLada.getText()));
            emp.setTEL_EMPLEADO(Integer.parseInt(txtTelefono.getText()));
            emp.setCORREO_EMPLEADO(txtCorreo.getText());
            emp.setRFC_EMPLEADO(txtRFC.getText());
            emp.setROL_EMPLEADO(txtRol.getText());
            emp.setEMPLEADO_SUCURSAL(Integer.parseInt((String) txtSuc.getSelectedItem()));
            //emp.setEMPLEADO_SUCURSAL(2); 
            emp.setEMPLEADO_DOMICILIO(Integer.parseInt(txtDomi.getText()));
            String mensaje;
            try {
                mensaje = ebo.modifiEmple(emp);
                JOptionPane.showMessageDialog(null,mensaje);
                limpiar();
                listarEmpleado();
            } catch (SQLException ex) {
                Logger.getLogger(FrmEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }     
        }   
        
    }//GEN-LAST:event_btnModifActionPerformed

